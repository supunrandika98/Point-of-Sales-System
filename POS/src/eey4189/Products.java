    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        
        String name = txtname.getText();
        String dis = txtdis.getText();
        String price = txtprice.getText();
        String brand = txtbrand.getSelectedItem().toString();
        String status = txtstatus.getSelectedItem().toString();
        String quantity = txtquantity.getText();
        String barcode = txtbarcode.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3308/pos","root", "");
            
            
            String sql ="select * from products where ProductName= '"+ name+"' OR Barcode='"+barcode+"'";
            ResultSet rs = ps1.executeQuery(sql);
            
            if(rs.next()){
            
            JOptionPane.showMessageDialog(null, "This Product Already Exist!");     
            
            
            }
            else{
            ps1 = con1.prepareStatement("insert into products (ProductName,Description,Price,Brand,Status,Quantity,Barcode) values (?,?,?,?,?,?,?)");
        
            ps1.setString(1,name);
            ps1.setString(2,dis);
            ps1.setString(3,price);
            ps1.setString(4,brand);
            ps1.setString(5,status);
            ps1.setString(6,quantity);
            ps1.setString(7,barcode);
           
            ps1.executeUpdate();
            table_update();
                      
                           
           txtname.setText("");
           txtdis.setText("");
           txtprice.setText("");
           txtbrand.setSelectedIndex(-1);
           txtstatus.setSelectedIndex(-1);
           txtquantity.setText("");
           txtbarcode.setText("");
           
           txtname.requestFocus();
         
           
           JOptionPane.showMessageDialog(null, "SUCCESSFULLY INSERTED!");          
            
            }
            
           
           

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "ERROR!", "ERROR" , JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_addActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel d1 = (DefaultTableModel) table.getModel();
        int selectIndex = table.getSelectedRow();
        int Product_ID = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());
       
        String name = txtname.getText();
        String dis = txtdis.getText();
        String price = txtprice.getText();
        String brand = txtbrand.getSelectedItem().toString();
        String status = txtstatus.getSelectedItem().toString();
        String quantity = txtquantity.getText();
        String barcode = txtbarcode.getText();
        
       try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3308/pos","root", "");
            ps1 = con1.prepareStatement("update products set ProductName=?, Description = ?, Price=?, Brand=?, Status=?, Quantity =?,Barcode=? where Product_ID=?");
             ps1.setString(1,name);
            ps1.setString(2,dis);
            ps1.setString(3,price);
            ps1.setString(4,brand);
            ps1.setString(5,status);
            ps1.setString(6,quantity);
            ps1.setString(7,barcode);
            ps1.setInt(8, Product_ID);
           
           
            ps1.executeUpdate();
            table_update();
                      
            txtname.setText("");
            txtdis.setText("");
            txtprice.setText("");
            txtbrand.setSelectedIndex(-1);
            txtstatus.setSelectedIndex(-1);
            txtquantity.setText("");
            txtbarcode.setText("");
            
            txtname.requestFocus();
         
            JOptionPane.showMessageDialog(null, "SUCCESSFULLY UPDATED!");
            
           

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "ERROR!", "ERROR" , JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_updateActionPerformed

